-- Migration to create warmup_seeds table and migrate data from seed_list
-- This addresses the "missing" warmup_seeds table and populates it from the master list.

CREATE TABLE IF NOT EXISTS warmup_seeds (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    email TEXT NOT NULL UNIQUE,
    provider TEXT DEFAULT 'other',
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Copy existing data from seed_list to warmup_seeds to preserve what we already have
-- This ensures that the 230k+ emails imported in 0011_seed_import.sql are available.
INSERT INTO warmup_seeds (email, provider, created_at)
SELECT email, provider, created_at FROM seed_list
ON CONFLICT (email) DO NOTHING;

-- Update the RPC to use the new warmup_seeds table
-- Optimized function to get a random seed using offset for better performance on large tables
CREATE OR REPLACE FUNCTION get_random_seed()
RETURNS TABLE (email TEXT)
LANGUAGE plpgsql
AS $$
DECLARE
  total_count INTEGER;
  random_offset INTEGER;
BEGIN
  SELECT count(*) INTO total_count FROM warmup_seeds;
  
  IF total_count = 0 THEN
    RETURN;
  END IF;

  random_offset := floor(random() * total_count);
  
  RETURN QUERY
  SELECT s.email
  FROM warmup_seeds s
  OFFSET random_offset
  LIMIT 1;
END;
$$;

